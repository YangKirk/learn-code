st_r = "www.runoob.com"
print(st_r.upper())  # 把所有字符中的小写字母转换成大写字母
print(st_r.lower())  # 把所有字符中的大写字母转换成小写字母
print(st_r.capitalize())  # 把第一个字母转化为大写字母，其余小写
print(st_r.title())  # 把每个单词的第一个字母转化为大写，其余小写
